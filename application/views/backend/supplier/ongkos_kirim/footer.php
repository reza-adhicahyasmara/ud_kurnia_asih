<script type="text/javascript">
       
    var url = window.location;
    $('ul.nav-sidebar a').filter(function() {
        return this.href == url;
    }).addClass('active ');
    $('ul.nav-treeview a').filter(function() {
        return this.href == url;
    }).parentsUntil(".nav-sidebar > .nav-treeview").addClass('menu-open').prev('a').addClass('active');

    $("#ongkir_supplier").on("input", function(){
        var regexp = /[^0-9]/g;
        if($(this).val().match(regexp)){
            $(this).val( $(this).val().replace(regexp,'') );
        }
    });

    $("#berat_ongkir_supplier").on("input", function(){
        var regexp = /[^0-9]/g;
        if($(this).val().match(regexp)){
            $(this).val( $(this).val().replace(regexp,'') );
        }
    });

</script>


<script type="text/javascript">
    $(document).ready(function() {
        $('#btn_simpan_ongkir').on("click",function(){
            $('#form_ongkir').validate({
                rules: {
                    ongkir_supplier: {
                        required: true,
                        minlength: 5,
                    },
                    berat_ongkir_supplier: {
                        required: true,
                    },
                },
                messages: {
                    ongkir_supplier: {
                        required: "Ongkir harus diisi",
                        minlength: "Minimal 5 karakter",
                    },
                    berat_ongkir_supplier: {
                        required: "Berat harus diisi",
                    },
                },
                errorElement: 'span',
                errorPlacement: function (error, element) {
                    error.addClass('invalid-feedback');
                    element.closest('.form-group').append(error);
                },
                highlight: function (element, errorClass, validClass) {
                    $(element).addClass('is-invalid');
                },
                unhighlight: function (element, errorClass, validClass) {
                    $(element).removeClass('is-invalid');
                },
                submitHandler: function() {
                    $.ajax({
                        url : '<?php echo base_url('supplier/ongkos_kirim/edit_supplier'); ?>',
                        method: 'POST',
                        data : $('#form_ongkir').serialize(),
                        success: function(response){
                            if(response==1){
                                Swal.fire({
                                    icon: 'success',
                                    title: 'Berhasil!',
                                    text: 'Data telah diedit',
                                    showConfirmButton: true,
                                    confirmButtonColor: '#6f42c1',
                                    timer: 3000
                                }).then(function(){
                                    location.reload();
                                });
                            }else{
                                Swal.fire({
                                    icon: 'error',
                                    title: 'Gagal!',
                                    text: 'Response',
                                    showConfirmButton: true,
                                    confirmButtonColor: '#6f42c1',
                                    timer: 3000
                                })
                            }
                        } 
                    });   
                }
            });
        });
    });
</script>

